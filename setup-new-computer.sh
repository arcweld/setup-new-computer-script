    if grep --quiet "setup-new-computer.sh" ~/.bash_profile; then
        echo "✔ .bash_profile already modified. Skipping"
    else
        writetoBashProfile
        echo "✔ Added to .bash_profile"
    fi
printDivider

# Create .bash_aliases if it does not exist
printHeading "Prep Bash Aliases"
printDivider
